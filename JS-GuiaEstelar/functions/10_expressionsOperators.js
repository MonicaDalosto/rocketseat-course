/* Expressões e operadores:

- Expressions
- Operators
    Binary
    Unary
    Ternary
*/

// expression
let number = 1 

// Binary, para ser Binário é necessário dois valores, para que o operador seja posicionado entre eles.
console.log(number + 1) 

// Unary, para ser unária, a expressão deve possuir somente um elemento e utilizar os operadores de incremento ++
console.log(++number)
// ou decremento -- para modificá-lo 
console.log(--number)
// outro exemplo de operador unário
console.log(typeof number)

// Ternary, para ser ternária, a expressão exige três elementos, só temos um exemplo de expressão ternária (Se, então, senão)
console.log(true ? 'alo' : 'nada')
