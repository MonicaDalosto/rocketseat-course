// Operadores de atribuição (Assignment)

let x
console.log(x)

//  =   assignment
x = 1
console.log(x)

//  +=  addition assignment
// x = x + 2
x += 2
console.log(x)

//  -=  subtraction assignment
// x = x - 1
x -= 1
console.log(x)

//  *=   multiplication assignment
// x = x * 2
x *= 2
console.log(x)

//  /=  division assignment
//  x = x / 2
x = x / 2
console.log(x)

//  %=  remainder assignment
// x = x % 2
x %= 2
console.log(x)

//  **= exponentiation assignment
// x = x ** 2
x **= 2
console.log(x)
